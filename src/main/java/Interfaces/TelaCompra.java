    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection c;
            c = java.sql.DriverManager.getConnection(CDB.conecte);
            Statement s = c.createStatement();
            ResultSet rs = s.executeQuery("SELECT *FROM medicamentos WHERE Cod_Med = '"+codigo_med.getText()+"';");
            while (rs.next()) {
                obj.setValor_Unit((rs.getDouble("Valor_Custo")));
            }
            obj.setValorTotal(obj.getQntd_Med()*obj.getValor_Unit());
            valor_total.setText(obj.getValorTotal().toString());
            valor_unitario.setText(obj.getValor_Unit().toString());
            }catch (Exception e) {
            System.out.println(e.getMessage());
        }finally{
            obj.setValorTotal(obj.getQntd_Med()*obj.getValor_Unit());
            valor_total.setText(obj.getValorTotal().toString());
            valor_unitario.setText(obj.getValor_Unit().toString());
        } 

    }//GEN-LAST:event_jButton1ActionPerformed

