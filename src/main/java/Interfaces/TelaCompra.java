    private void pedidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pedidosActionPerformed
        // TODO add your handling code here:

        obj.setCnpj_Lab(cnpj.getText());
        obj.setFormaPag_L(forma_de_pagamento.getText());
        obj.setDataEntega_L((data_de_entrega.getText()));
        obj.setCod_Med(codigo_med.getText());
        obj.setQntd_Med((int) quantidade.getValue());
        obj.setDataCompra_L(data_compra.getText());


        obj.setTotalNota_L(Double.valueOf(nota_fiscal.getText()));

        obj.setValorTotal(Double.valueOf(valor_total.getText()));
        valor_unitario.getText();

        boolean a;
        a = CDB.newCompra(obj.getCnpj_Lab(), obj.getFormaPag_L() ,obj.getDataEntega_L() , obj.getDataCompra_L(),  obj.getTotalNota_L().toString(), obj.getCod_Med(), obj.getQntd_Med(), obj.getValor_Unit(), obj.getValorTotal());
        if (a) {
            obj.setNumNotaFiscal_L(numero_nota_fiscal.getText());
            try {
            Connection c;
            c = java.sql.DriverManager.getConnection(CDB.conecte);
            Statement s = c.createStatement();
            ResultSet rs = s.executeQuery("SELECT *FROM Pedidos_Compra WHERE Cod_Med='" + cnpj.getText() + "'");
            while (rs.next()) {
                obj.setNumNotaFiscal_L(rs.getString("NumNotaFiscal_L"));
            }
            } catch (Exception e) {
        }finally{
                numero_nota_fiscal.setText(obj.getNumNotaFiscal_L());
            model.addLinha(obj);
            cnpj.requestFocus();
            }
        }


    }//GEN-LAST:event_pedidosActionPerformed

